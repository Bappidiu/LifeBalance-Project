import React, { useContext, useEffect, useState } from 'react'
import { useParams, useNavigate } from 'react-router-dom' // Added useNavigate
import { AppContext } from '../context/AppContext'
import { assets } from '../assets/assets_frontend/assets'
import RelatedDoctors from '../components/RelatedDoctors'
import { toast } from 'react-toastify'

const Appoinment = () => {

  const { docId } = useParams()
  const { doctors, currencySymbol, getDoctorsData, bookAppointment, token } = useContext(AppContext) // Added bookAppointment, token
  const daysOfWeek = ['SUN', 'MON', 'TUE', 'WED', 'THU', 'FRI', 'SAT']
  const navigate = useNavigate() // Initialize navigate

  const [docInfo, setDocInfo] = useState(null)
  const [docSlots, setDocSlots] = useState([])
  const [slotIndex, setSlotIndex] = useState(0)
  const [slotTime, setSlotTime] = useState('')

  const fetchDocInfo = async () => {
    const docInfo = doctors.find(doc => doc._id === docId)
    setDocInfo(docInfo)
  }

  const getAvailableSlots = async () => {
    setDocSlots([])
    // ... (Keep your existing slot logic here, it looked correct) ... 
    // For brevity in chat, I am assuming the slot logic you sent is here.
    // Copy-paste your existing "getAvailableSlots" logic here.
    let today = new Date()
    for(let i = 0 ; i < 7 ; i++){
        let currentDate = new Date(today)
        currentDate.setDate(today.getDate() + i)
        let endTime = new Date()
        endTime.setDate(today.getDate() + i)
        endTime.setHours(24,0,0,0)
        if(today.getDate() === currentDate.getDate()){
          currentDate.setHours(currentDate.getHours() > 10 ? currentDate.getHours() + 1 : 10)
          currentDate.setMinutes(currentDate.getMinutes() > 30 ? 30 :0)
        } else{
          currentDate.setHours(10)
          currentDate.setMinutes(0)
        }
        let timeSlots = []
        while(currentDate < endTime){
          let formattedTime = currentDate.toLocaleTimeString([],{ hour: '2-digit', minute:'2-digit'})
          
          // Check if slot is available (from backend data)
          let day = currentDate.getDate()
          let month = currentDate.getMonth() + 1
          let year = currentDate.getFullYear()
          const slotDate = day + "_" + month + "_" + year
          const slotTime = formattedTime

          const isSlotAvailable = docInfo.slots_booked[slotDate] && docInfo.slots_booked[slotDate].includes(slotTime) ? false : true

          if(isSlotAvailable){
             timeSlots.push({
              datetime : new Date(currentDate),
              time : formattedTime
            })
          }
          currentDate.setMinutes(currentDate.getMinutes() + 30)
        }
        setDocSlots(prev => ([...prev , timeSlots]))
    }
  }

  const bookAppt = async () => {
    if(!token){
        toast.warn("Login to book appointment")
        return navigate('/login')
    }
    if(!slotTime){
        toast.warn("Select a time slot")
        return;
    }

    const date = docSlots[slotIndex][0].datetime
    let day = date.getDate()
    let month = date.getMonth() + 1
    let year = date.getFullYear()
    const slotDate = day + "_" + month + "_" + year

    const success = await bookAppointment(docId, slotDate, slotTime)
    if(success){
        navigate('/my-appoinments') // Redirect to My Appointments page
    }
  }

  useEffect(() => {
    fetchDocInfo()
  }, [doctors, docId])

  useEffect(() => {
    if(docInfo) getAvailableSlots()
  }, [docInfo])

  return docInfo && (
    <div>
        {/* Doctor Details */}
        <div className='flex flex-col sm:flex-row gap-4'>
          <div>
              <img className='bg-primary w-full sm:max-w-72 rounded-lg' src={docInfo.image} alt="" />
          </div>
          <div className='flex-1 border border-gray-400 rounded-lg p-8 py-7 bg-white mx-2 sm:mx-0 mt-[80px] sm:mt-0'>
            <p className='flex items-center gap-2 text-2xl font-medium text-gray-900'>{docInfo.name} <img className='w-5' src={assets.verified_icon} alt="" /></p>
            <div className='flex items-center gap-2 text-sm mt-1 text-gray-600'>
              <p>{docInfo.degree} - {docInfo.speciality}</p>
              <button className='py-0.5 px-2 border text-xs rounded-full'>{docInfo.experience}</button>
            </div>
            <div>
              <p className='flex items-center gap-1 text-sm font-medium text-gray-900 mt-3'>About <img src={assets.info_icon} alt="" /></p>
              <p className='text-sm text-gray-500 max-w-[700px] mt-1'>{docInfo.about}</p>
            </div>
            <p className='text-gray-500 font-medium mt-4'>
              Appointment Fee: <span className='text-gray-600'>{currencySymbol}{docInfo.fees}</span>
            </p>
          </div>
        </div>

      {/* Booking slots */}
      <div className='sm:ml-72 sm:pl-4 mt-4 font-medium text-gray-700'>
        <p>Booking slots</p>
        <div className='flex gap-3 items-center w-full overflow-x-scroll mt-4'>
          {docSlots.length && docSlots.map((item, index) => (
              <div onClick={() => setSlotIndex(index)} className={`text-center py-6 min-w-16 rounded-full cursor-pointer ${slotIndex === index ? 'bg-primary text-white' : 'border border-gray-200'}`} key={index}>
                 <p>{item[0] && daysOfWeek[item[0].datetime.getDay()]}</p>
                 <p>{item[0] && item[0].datetime.getDate()}</p>
              </div>
            ))}
        </div>
        <div className='flex items-center gap-3 w-full overflow-x-scroll mt-4'>
          {docSlots.length && docSlots[slotIndex].map((item, index) => (
            <p onClick={() => setSlotTime(item.time)} className={`text-sm font-light flex-shrink-0 px-5 py-2 rounded-full cursor-pointer ${item.time === slotTime ? 'bg-primary text-white' : 'text-gray-400 border border-gray-300'}`} key={index}>
              {item.time.toLowerCase()}
            </p>
          ))}
        </div>
        {/* UPDATED BUTTON HERE */}
        <button onClick={bookAppt} className='bg-primary text-white text-sm font-light px-14 py-3 rounded-full hover:scale-105 transition-all duration-300 my-6'>Book Appointment</button>
      </div>

      <RelatedDoctors docId={docId} speciality={docInfo.speciality} />
    </div>
  )
}

export default Appoinment